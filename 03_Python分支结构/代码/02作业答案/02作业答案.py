# 6, 给定一个5位数，分别把这个数字的万位，千位，百位、十位、个位算出来并显示。如： 12345
#    提示： 可以使用运算符整除// 和 求余%
n = 12345
a = n // 10000  # 万位
b = n // 1000 % 10  # 千位
c = n // 100 % 10  # 百位
d = n // 10 % 10  # 十位
e = n % 10  # 个位

print(a, b, c, d, e)


