

# Python算术运算符：
#    +  -  *  /    %余数  //（整除:向下取整）   **次方
print(10 + 4)  # 14
print(10 - 4)  # 6
print(10 * 4)  # 40
print(10 / 4)  # 2.5
print(10 % 4)  # 2
print(10 // 4)  # 2
print(10 ** 4)  # 10000

