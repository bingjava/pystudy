# 练习：从控制台输入一个三位数，取出个位，十位，百位
n = int(input('输入一个三位数：'))
# n = 123
a = n // 100  # 百位
b = n // 10 % 10  # 十位
c = n % 10  # 个位
print(a, b, c)
